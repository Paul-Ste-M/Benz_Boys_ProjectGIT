package com.model;

/**
 * An enum that represents the genre of a song.
 * @author Benz Boyz
 */
public enum Genre {

    /**
     * Represents Soul music.
     */
    SOUL,

    /**
     * Represents Jazz music.
     */
    JAZZ,

    /**
     * Represents Rock music.
     */
    ROCK,

    /**
     * Represents Pop music.
     */
    POP,

    /**
     * Represents music made for children.
     */
    KIDS
}

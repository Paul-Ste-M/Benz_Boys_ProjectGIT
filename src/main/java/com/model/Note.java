package com.model;

public class Note {
    private Pitch pitch;
    private Type type;
    private int noteWorth;
    private boolean halfBeat;
    private boolean isMinor;

    public Note() {
        this.pitch = pitch;
        this.type = type;
        this.noteWorth = noteWorth;
        this.halfBeat = halfBeat;
        this.isMinor = isMinor;
    }

    
}
